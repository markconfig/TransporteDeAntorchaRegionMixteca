    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnGuardarCambiosActionPerformed

        if ("".equals(txtClave.getText()) || "".equals(txtUser.getText()) || "".equals(txtPassword.getText())) {
            JOptionPane.showMessageDialog(null, "Favor de llenar todos los campos solicitados", "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {

            try {
                int indice = jDUserRegister.tableUsers.getSelectedRow();
                String ruta = "src" + barra + "Users" + barra + "Users.txt";
                TableModel Modelo = jDUserRegister.tableUsers.getModel();
                Modelo.setValueAt(txtPassword.getText(), jDUserRegister.tableUsers.getSelectedRow(), 2);

                FileWriter fw = new FileWriter(ruta);

                BufferedWriter bw = new BufferedWriter(fw);

                for (int i = 0; i < jDUserRegister.tableUsers.getRowCount(); i++) {// rows
                    for (int j = 0; j < jDUserRegister.tableUsers.getColumnCount(); j++) {// columns
                        bw.write(jDUserRegister.tableUsers.getValueAt(i, j).toString() + ",");
                    }
                    bw.newLine();
                }

                bw.close();
                fw.close();
                this.dispose();

            } catch (IOException ex) {
                Logger.getLogger(jDUserRegister.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }// GEN-LAST:event_btnGuardarCambiosActionPerformed

