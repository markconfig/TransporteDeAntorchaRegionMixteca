    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSaveActionPerformed
        if ("".equals(txtClave.getText()) || "".equals(txtUser.getText()) || "".equals(txtPassword.getText())) {
            JOptionPane.showMessageDialog(null, "Favor de llenar todos los campos solicitados", "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {

            String user = txtUser.getText();
            String filePath = "src" + barra + "Users" + barra + user + ".txt";
            String ruta = "src" + barra + "Users" + barra + "Users.txt";

            File file = new File(filePath);
            if (file.exists()) {
                JOptionPane.showMessageDialog(this, "Lo sentimos, ya hay un usuario con el mismo nombre.");
            } else {
                jDUserRegister.AgregarRegistroTabla(
                        new Object[] { txtClave.getText(), txtUser.getText(), txtPassword.getText() });
                try {
                    FileWriter fw = new FileWriter(file);

                    BufferedWriter bw = new BufferedWriter(fw);

                    bw.write(txtClave.getText() + "," + txtUser.getText() + "," + txtPassword.getText());
                    bw.newLine();
                    /**
                     * for (int i = 0; i <
                     * jDUserRegister.tableUsers.getRowCount(); i++) {//rows for
                     * (int j = 0; j <
                     * jDUserRegister.tableUsers.getColumnCount(); j++)
                     * {//columns
                     * bw.write(jDUserRegister.tableUsers.getValueAt(i,
                     * j).toString() + " "); } bw.newLine(); }*
                     */

                    bw.close();
                    fw.close();
                    JOptionPane.showMessageDialog(null, "Nuevo usuario registrado correctamente", "Nuevo Registro",
                            JOptionPane.INFORMATION_MESSAGE);
                } catch (IOException ex) {
                    Logger.getLogger(jDUserRegister.class.getName()).log(Level.SEVERE, null, ex);
                }

                // this.setDefaultCloseOperation(EXIT_ON_CLOSE);
                this.dispose();

                try {
                    FileWriter fw = new FileWriter(ruta);

                    BufferedWriter bw = new BufferedWriter(fw);

                    for (int i = 0; i < jDUserRegister.tableUsers.getRowCount(); i++) {// rows
                        for (int j = 0; j < jDUserRegister.tableUsers.getColumnCount(); j++) {// columns
                            bw.write(jDUserRegister.tableUsers.getValueAt(i, j).toString() + ",");
                        }
                        bw.newLine();
                    }

                    bw.close();
                    fw.close();

                } catch (IOException ex) {
                    Logger.getLogger(jDUserRegister.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
    }// GEN-LAST:event_btnSaveActionPerformed

